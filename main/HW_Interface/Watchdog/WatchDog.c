extern bool begin_active_device;

/*******************************************************************************
 * Application Funtions
 ******************************************************************************/
void ESP_resetChip()
{
    if (!begin_active_device) {
        Flash_saveWaitTimeActive();
    }
    printf("Reset Chip After 10ms...\n");
    vTaskDelay(10/portTICK_PERIOD_MS);
    esp_restart();
}

